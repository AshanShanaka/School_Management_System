  static const Color primaryLight = Color(0xFF818CF8);
  static const Color primaryDark = Color(0xFF4F46E5);

  // Secondary Colors
  static const Color secondary = Color(0xFFFACC15); // Yellow
  static const Color secondaryLight = Color(0xFFFDE047);

  // Teacher Color
  static const Color teacher = Color(0xFF8B5CF6); // Purple

  // Student Color
  static const Color student = Color(0xFF3B82F6); // Blue

  // Parent Color
  static const Color parent = Color(0xFFF59E0B); // Amber

  // Status Colors
  static const Color success = Color(0xFF10B981); // Green
  static const Color warning = Color(0xFFF59E0B); // Amber
  static const Color error = Color(0xFFEF4444); // Red
  static const Color info = Color(0xFF3B82F6); // Blue

  // Grade Colors
  static const Color gradeA = Color(0xFF10B981); // Green
  static const Color gradeB = Color(0xFF3B82F6); // Blue
  static const Color gradeC = Color(0xFFF59E0B); // Amber
  static const Color gradeS = Color(0xFF8B5CF6); // Purple
